<?php

namespace BibliothequeBundle\Entity;

/**
 * Faculte
 */
class Faculte
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var int
     */
    private $cycle;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set cycle
     *
     * @param integer $cycle
     *
     * @return Faculte
     */
    public function setCycle($cycle)
    {
        $this->cycle = $cycle;

        return $this;
    }

    /**
     * Get cycle
     *
     * @return int
     */
    public function getCycle()
    {
        return $this->cycle;
    }
}
